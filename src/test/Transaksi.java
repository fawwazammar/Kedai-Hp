    private void tfJumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfJumlahActionPerformed
        // TODO add your handling code here:
        int stok = Integer.parseInt(lStok.getText());
        int jumlah = Integer.parseInt(tfJumlah.getText());
        
        if(tfJumlah.getText().equals("")){
            tfSubtotal.setText("");
        }else if(jumlah >= stok){
            JOptionPane.showMessageDialog(this, "Stok tidak mencukupi");
        }else{
            int harga = Integer.parseInt(lHarga.getText());
            int jumlahH = Integer.parseInt(tfJumlah.getText());
            int subtotal = harga*jumlahH;

            tfSubtotal.setText(String.valueOf(subtotal));            
        }
    }//GEN-LAST:event_tfJumlahActionPerformed

