    private void bProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bProsesActionPerformed
        // TODO add your handling code here:
        int t = jTabelTransaksi.getRowCount();
        try{
            for(int i=0;i<t;i++){
                String kodeHp = jTabelTransaksi.getValueAt(i, 0).toString();
                String merkHp = jTabelTransaksi.getValueAt(i, 1).toString();
                String tipe = jTabelTransaksi.getValueAt(i, 2).toString();
                int harga = Integer.parseInt(jTabelTransaksi.getValueAt(i, 3).toString());
                int jml = Integer.parseInt(jTabelTransaksi.getValueAt(i, 4).toString());
                int subtot = Integer.parseInt(jTabelTransaksi.getValueAt(i, 5).toString());

                stat.executeUpdate("INSERT INTO transaksi VALUES("
                    + "'" + lNoTr.getText() + "',"
                    + "'" + kodeHp + "',"
                    + "'" + lTglTr.getText()+ "',"
                    + "'" + merkHp+ "',"
                    + "'" + tipe+ "',"
                    + "'" + harga+ "',"
                    + "'" + jml+ "',"
                    + "'" + subtot+ "',"
                    + "'" + tfTotal.getText()+ "',"
                    + "'" + tfBayar.getText()+ "',"
                    + "'" + tfKembalian.getText()+ "')");

                String select = "SELECT jumlah FROM handphone WHERE kode_hp='"+kodeHp+"'";
                ResultSet rs   = stat.executeQuery(select);
                int jumlahbeli = Integer.parseInt(tfJumlah.getText());
                int stok = 0;
                while(rs.next()){
                    stok = rs.getInt("jumlah");
                }
                int total = stok - jumlahbeli;

                String update="UPDATE handphone SET jumlah='"+total+"' where kode_hp='"+kodeHp+"'";
                stat.executeUpdate(update);

                JOptionPane.showMessageDialog(this, "TRANSAKSI BERHASIL");

                tabel();
            }
        }catch (Exception e) {
            Logger.getLogger(Transaksi.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null, "SIMPAN TRANSAKSI PENJUALAN GAGAL");
        }finally {
            this.dispose();
            new Transaksi().setVisible(true);
        }
    }//GEN-LAST:event_bProsesActionPerformed

